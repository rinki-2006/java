public class Bitwiseoperators {
    public static void main(String[] args) {
          int a = 6;
          int b = 1;
//        byte a = 0b1010;
//        byte b = 0b01;
//        System.out.println(Integer.toBinaryString(a&b));
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(a<<b);
        System.out.println(a>>b);

    }
}
